
x = 10
y = 15

print(x == y)   # returns TRUE if both are same else FALSE

print(x != y)   # returns TRUE if both are not same else FALSE

print(x > y)   # returns TRUE if x is greater than y else FALSE

print(x < y)   # returns TRUE if x is less than y else FALSE

print(x >= y)   # returns TRUE if x is greater than or equal to y else FALSE

print(x <= y)   # returns TRUE if is x less than or equal to y else FALSE
